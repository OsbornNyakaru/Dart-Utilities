void checkString(String value) {
  switch (value) {
    case 'A':
      print('Value is A');
      break;
    case 'B':
      print('Value is B');
      break;
    case 'C':
      print('Value is C');
      break;
    case 'D':
      print('Value is D');
      break;
    case 'E':
      print('Value is E');
      break;
    case 'F':
      print('Value is F');
      break;
    case 'G':
      print('Value is G');
      break;
    default:
      print('Value is none of the following: A, B, C, D, E, F, G');
  }
}
